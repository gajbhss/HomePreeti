package Java_Package;

public class while_loop {

	public static void main(String[] args) 
	{
	
		int i=1,j=10;// Initialization
		
		while(i<=10)//Condition
		{
			System.out.println("i:"+i)
			;
			i++;//Increment/Decrements
		}
		
		System.out.println("");
		
		while(j>=1)
		{
			System.out.println("j :"+j);
			j--;
		}

	}

}
